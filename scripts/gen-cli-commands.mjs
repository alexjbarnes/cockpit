#!/usr/bin/env node
// Regenerate src/lib/cli-commands.ts from the installed Claude CLI binary.
//
// The CLI bundles every built-in slash command as an object literal carrying a
// `type` field: "prompt" (runs a model turn), "local" (acts in the CLI, no model
// turn), or "local-jsx" (renders an interactive Ink panel). cockpit routes on
// that type in PTY mode (see session-manager handleCommand), so this map has to
// track the CLI it runs against. Regenerate after a CLI upgrade:
//
//   node scripts/gen-cli-commands.mjs > src/lib/cli-commands.ts && npx biome format --write src/lib/cli-commands.ts
//
// Binary path: argv[2], or $CLAUDE_BIN, or `command -v claude` (resolved).

import { execSync } from "node:child_process";
import { readFileSync, realpathSync } from "node:fs";

function resolveBin() {
  if (process.argv[2]) return process.argv[2];
  if (process.env.CLAUDE_BIN) return process.env.CLAUDE_BIN;
  const which = execSync("command -v claude", { encoding: "utf8" }).trim();
  return realpathSync(which);
}

const bin = resolveBin();
const data = readFileSync(bin, "latin1");

const version = (execSync(`"${bin}" --version`, { encoding: "utf8" }).match(/(\d+\.\d+\.\d+)/) || [])[1] || "unknown";

// Anchor on each `name:"..."`, then read a window that extends BOTH directions
// (the CLI orders object fields inconsistently -- `type` can precede or follow
// `name`). Clamp the window to the adjacent name tokens so one object can't pick
// up a neighbour's `type`. A command object carries both `type` and description.
const nameRe = /name:"([a-z][a-z0-9-]{0,40})"/g;
const namePos = [...data.matchAll(nameRe)].map((m) => ({ start: m.index, end: m.index + m[0].length, name: m[1] }));

/** @type {Record<string, {type: string, aliases: Set<string>, hidden: boolean}>} */
const cmds = {};
for (let i = 0; i < namePos.length; i++) {
  const { start, end, name } = namePos[i];
  const left = Math.max(i > 0 ? namePos[i - 1].end : 0, start - 400);
  const right = Math.min(i < namePos.length - 1 ? namePos[i + 1].start : data.length, end + 900);
  const win = data.slice(left, right);
  const tm = win.match(/type:"(local-jsx|local|prompt)"/);
  if (!tm || !win.includes("description")) continue; // command objects carry both
  const rec = cmds[name] || { type: tm[1], aliases: new Set(), hidden: false };
  rec.type = tm[1];
  if (/isHidden:(!0|\(\)=>!0|true)/.test(win)) rec.hidden = true;
  cmds[name] = rec;
}

// Aliases sit adjacent to their owner's name (`name:"X",aliases:[...]`). Attribute
// each alias array to the NEAREST name token, not any window that contains it --
// a wide window would let a neighbour claim it (and produce duplicate keys).
for (const am of data.matchAll(/aliases:\[([^\]]*)\]/g)) {
  const pos = am.index;
  let owner = null;
  let dist = Infinity;
  for (const np of namePos) {
    const d = pos >= np.start && pos <= np.end ? 0 : Math.min(Math.abs(pos - np.end), Math.abs(np.start - pos));
    if (d < dist) { dist = d; owner = np; }
    if (np.start > pos && d > dist) break; // namePos is sorted; past the nearest
  }
  if (!owner || dist > 250 || !cmds[owner.name]) continue;
  for (const a of am[1].matchAll(/"([a-z][a-z0-9-]*)"/g)) cmds[owner.name].aliases.add(a[1]);
}

// aliases must not collide with a real command name
const names = new Set(Object.keys(cmds));
const aliasPairs = [];
for (const [name, rec] of Object.entries(cmds)) {
  for (const a of rec.aliases) if (!names.has(a)) aliasPairs.push([a, name]);
}
aliasPairs.sort((x, y) => x[0].localeCompare(y[0]));

const sorted = Object.keys(cmds).sort();
const typeLines = sorted.map((n) => `  ${JSON.stringify(n)}: ${JSON.stringify(cmds[n].type)},`).join("\n");
const aliasLines = aliasPairs.map(([a, n]) => `  ${JSON.stringify(a)}: ${JSON.stringify(n)},`).join("\n");

process.stdout.write(`// AUTO-GENERATED by scripts/gen-cli-commands.mjs from Claude CLI ${version}.
// Do not edit by hand. Regenerate after a CLI upgrade:
//   node scripts/gen-cli-commands.mjs > src/lib/cli-commands.ts && npx biome format --write src/lib/cli-commands.ts
//
// Built-in slash commands keyed by their CLI command \`type\`. This drives PTY
// routing: "prompt" commands run a model turn and fire a Stop hook (safe to
// forward), while "local" and "local-jsx" run in the CLI only -- the latter open
// an interactive panel cockpit can't render or drive, the former act with no
// Stop hook and would hang the session on "processing". Both are blocked in PTY.
// Commands not listed here (custom/project/plugin) are treated as pass-through.

export type CliCommandType = "prompt" | "local" | "local-jsx";

export const CLI_VERSION = ${JSON.stringify(version)};

export const CLI_COMMAND_TYPES: Record<string, CliCommandType> = {
${typeLines}
};

// Alias -> canonical command name (e.g. "/rc" -> "remote-control").
export const CLI_COMMAND_ALIASES: Record<string, string> = {
${aliasLines}
};

/** Classify a slash command (with or without leading "/") by its CLI type. */
export function classifyCliCommand(name: string): CliCommandType | "unknown" {
  const key = name.replace(/^\\//, "").toLowerCase();
  const canonical = CLI_COMMAND_ALIASES[key] ?? key;
  return CLI_COMMAND_TYPES[canonical] ?? "unknown";
}
`);
